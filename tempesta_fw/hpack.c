#define BIT_JOIN8(x, y)			((x) << 8 | (y))
#define BIT_SHIFT(x, y, z)		(((x) << (y)) | (z))
#define BIT_JOIN			BIT_SHIFT

#define HPACK_LIMIT			(64 / 7) * 7
#define HPACK_LAST			((1 << (64 % 7)) - 1)

